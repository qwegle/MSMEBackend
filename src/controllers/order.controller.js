const SettlementOrder = require('../models/settlementOrder');
const OTSForm = require('../models/otsform');
const AckForm = require('../models/acknowledgement');
const Memorandum = require('../models/memorandum');

exports.uploadSettlementOrder = async (req, res) => {
  try {

    const filePath = req.file ?`${process.env.NODE_APP_URL}/uploads/${req.file.filename}` : null;

    if (!filePath) {
      return res.status(400).json({ error: 'PDF file is required.' });
    }

    const { loan_number } = req.body;
    if (!loan_number) {
      return res.status(400).json({ error: 'loan_number is required.' });
    }
    const otsForm = await OTSForm.findOne({ loan_number });
    if (!otsForm) {
      return res.status(404).json({ error: 'OTSForm not found for provided loan_number.' });
    }
    const userId = otsForm.userId;
    const otsId = otsForm._id;
    const ackForm = await AckForm.findOne({ ots_form_id: otsId });
    if (!ackForm) {
      return res.status(404).json({ error: 'AckForm not found for this OTSForm.' });
    }
    const AckId = ackForm._id;
    const memo = await Memorandum.findOne({ ackId: AckId });
    if (!memo) {
      return res.status(404).json({ error: 'Memorandum not found for this AckForm.' });
    }
    if(memo.status == 1){
      return res.status(400).json({ error: 'Memorandum has not been approved for this Loan Number please approve before uploading settlement order' });
    }
    const existingOrder = await SettlementOrder.findOne({otsId});
    if(existingOrder){
      return res.status(404).json({ error: 'Settlement Order Already exists for this Loan Number.' });
    }
    const memoId = memo._id;
    const newOrder = new SettlementOrder({
      userId,
      otsId,
      AckId,
      memoId,
      pdfData: filePath,
    });
    const savedOrder = await newOrder.save();
    await OTSForm.findByIdAndUpdate(
      otsId,
      {
        status_msg: 'Settlement Order generated by admin office',
      },
      { new: true }
    );
    res.status(201).json({
      message: 'Settlement order created and application status updated successfully.',
      id: savedOrder._id,
    });
  } catch (error) {
    console.error('Upload Settlement Order Error:', error);
    res.status(500).json({ message: 'Internal Server Error', error: error.message });
  }
};

exports.getSettlementOrdersByUserId = async (req, res) => {
  try {
    const { userId } = req.params;

    if (!userId) {
      return res.status(400).json({ message: 'User ID is required' });
    }

    const orders = await SettlementOrder.find({ userId })
      .populate('otsId')
      .populate('AckId')
      .populate('memoId')
      .sort({ createdAt: -1 });

    if (!orders.length) {
      return res.status(404).json({ message: 'No settlement orders found for this user.' });
    }

    res.status(200).json(orders);
  } catch (error) {
    console.error('Get Settlement Orders by User ID Error:', error);
    res.status(500).json({ message: 'Internal Server Error' });
  }
};

exports.listSettlementOrders = async (req, res) => {
  try {
    const orders = await SettlementOrder.find()
      .populate('userId')
      .populate('otsId')
      .populate('AckId')
      .populate('memoId')
      .sort({ createdAt: -1 }); // Optional: sort by newest first

    res.status(200).json(orders);
  } catch (error) {
    console.error('List Settlement Orders Error:', error);
    res.status(500).json({ message: 'Internal Server Error' });
  }
};

exports.filterSettlementOrders = async (req, res) => {
  try {
    const { userId, loan_number } = req.body;
    let filter = {};
    if (userId && loan_number) {
      const ots = await OTSForm.findOne({ userId, loan_number });
      if (!ots) {
        return res.status(404).json({ message: 'No matching OTSForm found for provided userId and loan_number.' });
      }
      filter.userId = userId;
      filter.otsId = ots._id;
    }
    else if (userId) {
      filter.userId = userId;
    }
    else if (loan_number) {
      const ots = await OTSForm.findOne({ loan_number });
      if (!ots) {
        return res.status(404).json({ message: 'No OTSForm found for the provided loan_number.' });
      }
      filter.otsId = ots._id;
    }
    const orders = await SettlementOrder.find(filter)
      .populate('userId')
      .populate('otsId')
      .populate('AckId')
      .populate('memoId')
      .sort({ createdAt: -1 });

    if (!orders.length) {
      return res.status(404).json({ message: 'No settlement orders found for the provided filters.' });
    }

    res.status(200).json(orders);
  } catch (error) {
    console.error('Filter Settlement Orders Error:', error);
    res.status(500).json({ message: 'Internal Server Error', error: error.message });
  }
};
