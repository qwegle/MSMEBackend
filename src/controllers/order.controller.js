const SettlementOrder = require('../models/settlementOrder');
const OTSForm = require('../models/otsform');

exports.uploadSettlementOrder = async (req, res) => {
  try {
    const filePath = req.file ? `/uploads/${req.file.filename}` : null;
    if (!filePath) {
      return res.status(400).json({ message: 'PDF file is required.' });
    }
    const { userId, otsId, AckId, memoId } = req.body;
    const newOrder = new SettlementOrder({
      userId,
      otsId,
      AckId,
      memoId,
      pdf_link: filePath,
    });
    const savedOrder = await newOrder.save();
    if (otsId) {
      const updatedOtsForm = await OTSForm.findByIdAndUpdate(
        otsId,
        {
          status_msg: "Settlement Order generated by admin office",
        },
        { new: true }
      );
      if (!updatedOtsForm) {
        return res.status(404).json({ message: 'OTS form not found to update status.' });
      }
    }
    res.status(201).json({
      message: 'Settlement order created and application status updated successfully.',
      id: savedOrder._id,
    });
  } catch (error) {
    console.error('Upload Settlement Order Error:', error);
    res.status(500).json({ message: 'Internal Server Error', error: error.message });
  }
};

exports.getSettlementOrdersByUserId = async (req, res) => {
  try {
    const { userId } = req.params;

    if (!userId) {
      return res.status(400).json({ message: 'User ID is required' });
    }

    const orders = await SettlementOrder.find({ userId })
      .populate('otsId')
      .populate('AckId')
      .populate('memoId')
      .sort({ createdAt: -1 });

    if (!orders.length) {
      return res.status(404).json({ message: 'No settlement orders found for this user.' });
    }

    res.status(200).json(orders);
  } catch (error) {
    console.error('Get Settlement Orders by User ID Error:', error);
    res.status(500).json({ message: 'Internal Server Error' });
  }
};

exports.listSettlementOrders = async (req, res) => {
  try {
    const orders = await SettlementOrder.find()
      .populate('userId')
      .populate('otsId')
      .populate('AckId')
      .populate('memoId')
      .sort({ createdAt: -1 }); // Optional: sort by newest first

    res.status(200).json(orders);
  } catch (error) {
    console.error('List Settlement Orders Error:', error);
    res.status(500).json({ message: 'Internal Server Error' });
  }
};

